The buttons are available as widgets so that you can easily add them to the sidebar or footer.

<ul>
    <li>Go to <i>Appearance → Widgets</i></li>
    <li>Find the widget called <i>Menu - Ordering - Reservations</i> and add it to the sidebar / footer.<br>
        <img src="<?= plugins_url('/images/help/publishing/q3-1.png', dirname(__FILE__).'/../../../../')?>"></li>
    <li>In the small settings area, choose whether you want both buttons to appear or only one of them<br>
        <img src="<?= plugins_url('/images/help/publishing/q3-2.png', dirname(__FILE__).'/../../../../')?>"></li>

</ul>
Notes:
<ul>
    <li>You can add this widget to the container as many times you want</li>
    <li>If you want your buttons to have a special CSS in the sidebar / footer, click on “Custom CSS class” and pass the name of your custom CSS class, like:<br>
        <span class="code">your-css-class-name</span>
    </li>
</ul>
